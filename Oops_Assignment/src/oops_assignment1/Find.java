package oops_assignment1;

public class Find {
    public double find(float radius){
        return 3.14*radius*radius;
    }
    public int find(int side){
        return side*side;
    }
    public int find(int length, int width){
        return length*width;
    }
    public double find(float base, float height){
        return 0.5*base*height;
    }
}
